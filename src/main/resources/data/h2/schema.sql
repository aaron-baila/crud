DROP TABLE VEHICULOS IF EXISTS;
DROP SEQUENCE 	VEHICULOS_SEQ 	IF EXISTS;

CREATE SEQUENCE "VEHICULOS_SEQ"
	MINVALUE 1000
	MAXVALUE 999999999
	INCREMENT BY 50
	START WITH 1000
	NOCACHE
	NOCYCLE;

CREATE SEQUENCE "MANTENIMIENTOS_SEQ"
	MINVALUE 1000
	MAXVALUE 999999999
	INCREMENT BY 50
	START WITH 1000
	NOCACHE
	NOCYCLE;

CREATE TABLE VEHICULOS(
	ID					BIGINT			NOT NULL,
	MATRICULA			VARCHAR(100)	,
	MODELO				VARCHAR(100)	,
	KM			        BIGINT,
	PRIMARY KEY (ID)

	CREATE TABLE MANTENIMIENTOS(
    	ID					BIGINT			NOT NULL,
    	MATRICULA			VARCHAR(100)	,
    	MODELO				VARCHAR(100)	,
    	KM			        BIGINT,
    	PRIMARY KEY (ID)
);